#include<iostream>
using namespace std;
int main(){
    int a=10,b=10;
 
    cout<<"The sum of "<<a<<"+"<<b<<"= "<< (a+b)<<endl;
    cout<<"The difference of "<<a<<"-"<<b<<"= "<< (a-b)<<endl;
    cout<<"The product of "<<a<<"*"<<b<<"= "<< (a*b)<<endl;
    cout<<"The division of "<<a<<"/"<<b<<"= "<<( a/b)<<endl;
    cout<<"The Modulus of "<<a<<"%"<<b<<"= "<<(a%b)<<endl;
    return 0;
}